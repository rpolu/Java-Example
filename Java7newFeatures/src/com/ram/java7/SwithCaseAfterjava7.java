package com.ram.java7;

public class SwithCaseAfterjava7 {
	public static void main(String[] args) {

		String game = "ccc";
		switch (game) {
		case "Hockey":
			System.out.println("Let's play Hockey");
			break;
		case "Cricket":
			System.out.println("Let's play Cricket");
			break;
		case "Football":
			System.out.println("Let's play Football");
		default:
			System.out.println("Let's go for movie");
		}

	}

}
